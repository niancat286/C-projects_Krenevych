#include <stdio.h>


int main() {
    int a1 = 2+31;
    printf("2+31 = %d\n", a1);

    int a2 = 45*54-11;
    printf("45*54-11 = %d\n", a2);

    int a3 = 15/4;
    printf("15/4 = %d\n", a3);

    double a4 = 15.0/4;
    printf("15.0/4 = %lf\n", a4);

    int a5 = 67%5;
    printf("67 mod 5 = %d\n", a5);

    double a6 = (2*45.1 +3.2)/2;
    printf("(2*45.1 +3.2)/2 = %lf\n", a6);

    return 0;
}